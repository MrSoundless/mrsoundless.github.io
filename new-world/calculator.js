$('document').ready(function() {
    $('.gives-results').on('change', function() {
        var skill = $('#skill').val();
        var currentLevelXp = parseInt($('#currentLevelXp').val());
        var currentLevel = parseInt($('#currentLevel').val());
        var targetLevel = parseInt($('#targetLevel').val());
        var xpTable = getXpTable(skill);

        if (isNaN(currentLevelXp) || isNaN(currentLevel) || isNaN(targetLevel) || targetLevel <= currentLevel)
        {
            $('#success-message').hide();
            $('#error-message').show();
        }
        else {
            var total = currentLevelXp;
            for (var i=currentLevel; i<=targetLevel-1; ++i) {
                total += xpTable[i];
            }
            
            $('#totalXpRequired').text(total);
            $('#success-message').show();
            $('#error-message').hide();
        }
    });
});

function getXpTable(key) {
    switch (key) {
        case "arcana": return arcanaXp;
        case "armoring": return armoringXp;
        case "cooking": return cookingXp;
        case "engineering": return engineeringXp;
        case "furnishing": return furnishingXp;
        case "jewelCrafting": return jewelCraftingXp;
        case "weaponSmithing": return weaponSmithingXp;

        case "leatherWorking": return leatherWorkingXp;
        case "smelting": return smeltingXp;
        case "stoneCutting": return stoneCuttingXp;
        case "weaving": return weavingXp;
        case "woodWorking": return woodWorkingXp;

        case "fishing": return fishingXp;
        case "harvesting": return harvestingXp;
        case "logging": return loggingXp;
        case "mining": return miningXp;
        case "skinning": return skinningXp;
    }
    return null;
}